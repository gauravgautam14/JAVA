package Stringintro;

public class IndexofLastindexofCharATContainsStartswithEndwith {
	public static void main(String[] args) {
		String s = "gaurav";
		System.out.println(s.indexOf("u"));//2
		System.out.println(s.lastIndexOf("a"));//4
		System.out.println(s.charAt(3));//r
		System.out.println(s.contains("v"));//true
		System.out.println(s.startsWith("h"));//false
		System.out.println(s.endsWith("v"));//ture
	}
}
